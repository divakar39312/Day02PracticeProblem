package com.Bridgelabz;

public class NumberConversion {

	public static void main(String[] args) {
		int number=100;
		if(number==1) {
			System.out.println("units");
		}else if(number==10) {
			System.out.println("tens");
		}else if(number==100) {
			System.out.println("hundred");
		}else if(number==1000) {
			System.out.println("thousands");
		}
	}
}
